#include <lpc214x.h>

int main(void) {
    int count;
    unsigned int value;
    
    // 48-point 8-bit digital sine values (0x00 to 0xFF)
    unsigned char sine_tab[48] = {
        0x80, 0x90, 0xA1, 0xB1, 0xC0, 0xCD, 0xDA, 0xE5, 0xF6, 0xFB, 0xFE, 0xFF,
        0xFF, 0xFB, 0xF6, 0xEE, 0xE5, 0xDA, 0xCD, 0xC0, 0xB1, 0xA1, 0x90, 0x80,
        0x70, 0x5F, 0x4F, 0x40, 0x33, 0x26, 0x1B, 0x12, 0x05, 0x02, 0x00, 0x02,
        0x05, 0x0A, 0x12, 0x1B, 0x26, 0x33, 0x40, 0x4F, 0x5E, 0x70, 0x80
    };

    PINSEL0 = 0x00000000; // Configure Port 0.0 to 0.15 as GPIO
    PINSEL1 = 0x00000000; // Configure Port 0.16 to 0.31 as GPIO
    IO0DIR  = 0x00FF0000; // Set P0.16 to P0.23 as Output

    while(1) {
        for(count = 0; count < 48; count++) {
            // Mask, shift the 8-bit value to match pins P0.16-P0.23, and send to pins
            value = (sine_tab[count] << 16) & 0x00FF0000;
            IO0PIN = value;
        }
    }
    return 0;
}
